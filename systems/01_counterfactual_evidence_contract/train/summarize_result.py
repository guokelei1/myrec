#!/usr/bin/env python
"""Summarize the locked C01 screening after its one shared evaluator call."""

from __future__ import annotations

import argparse
import json
import os
import sys
from datetime import datetime, timezone
from pathlib import Path

CANDIDATE_ROOT = Path(__file__).resolve().parents[1]
REPO_ROOT = CANDIDATE_ROOT.parents[1]
sys.path.insert(0, str(CANDIDATE_ROOT))
sys.path.insert(0, str(REPO_ROOT / "src"))

from train.data import iter_jsonl, sha256_file  # noqa: E402
from train.integrity import CONFIG_PATH, load_config, verify_proposal_lock  # noqa: E402


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--config", default=str(CONFIG_PATH))
    return parser.parse_args()


def load_json(path: Path) -> dict:
    with path.open("r", encoding="utf-8") as handle:
        return json.load(handle)


def main() -> int:
    args = parse_args()
    os.chdir(REPO_ROOT)
    config = load_config(args.config)
    lock = verify_proposal_lock(config)
    run_dir = REPO_ROOT / config["paths"]["c01_run_dir"]
    artifact_root = REPO_ROOT / config["paths"]["c01_artifacts"]
    metrics = load_json(run_dir / "metrics.json")
    internal = load_json(artifact_root / "internal_gate_report.json")
    determinism = load_json(artifact_root / "determinism_first1000.json")
    preaudit = load_json(artifact_root / "pre_evaluation_audit.json")
    metadata = load_json(run_dir / "metadata.json")
    if metrics.get("generated_by") != "myrec.eval.evaluator":
        raise ValueError("result was not generated by the shared evaluator")
    if metrics.get("candidate_manifest_sha256") != config["paths"]["candidate_manifest_sha256"]:
        raise ValueError("evaluator candidate manifest hash mismatch")
    if metrics.get("scores_sha256") != metadata.get("scores_sha256"):
        raise ValueError("evaluator score hash mismatch")
    calls = [
        row
        for row in iter_jsonl(REPO_ROOT / "reports" / "dev_eval_log.jsonl")
        if str(row.get("run_id")) == config["run_id"]
    ]
    if len(calls) != 1:
        raise ValueError(f"expected exactly one matching dev evaluator call, got {len(calls)}")
    ndcg = float(metrics["ndcg@10"])
    d2p = float(config["screening"]["d2p_seed_ndcg10"])
    relative_gain = (ndcg - d2p) / d2p
    checks = {
        "aggregate_stop_loss": ndcg >= float(config["screening"]["ndcg10_stop_loss"]),
        "candidate_hash_integrity": metrics["candidate_manifest_sha256"] == config["paths"]["candidate_manifest_sha256"],
        "determinism": bool(determinism["serialized_bytes_identical"]),
        "exactly_one_dev_evaluator_call": len(calls) == 1,
        "internal_falsifier": bool(internal["ready_for_dev_scoring"]),
        "relative_gain_over_d2p": relative_gain >= float(config["screening"]["d2p_min_relative_gain"]),
    }
    survived = all(checks.values())
    report = {
        "candidate_hash": lock["candidate_hash"],
        "candidate_id": config["candidate_id"],
        "checks": checks,
        "decision": "advance-to-full-gate" if survived else "stop-dev-screening",
        "dev": {
            "d2p_seed_ndcg10": d2p,
            "item_only_seed_ndcg10": float(config["screening"]["item_only_seed_ndcg10"]),
            "ndcg10": ndcg,
            "relative_gain_over_d2p": relative_gain,
            "screening_stop_loss": float(config["screening"]["ndcg10_stop_loss"]),
        },
        "dev_evaluator_calls": len(calls),
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "internal_gate_items": internal["internal_falsifier"]["gate_items"],
        "metrics_path": str(run_dir / "metrics.json"),
        "metrics_sha256": sha256_file(run_dir / "metrics.json"),
        "pre_evaluation_audit_sha256": sha256_file(artifact_root / "pre_evaluation_audit.json"),
        "qrels_read_only_by_shared_evaluator": True,
        "run_id": config["run_id"],
        "status": "screening_complete_stop_after_report",
        "survived_screening": survived,
        "test_files_read": False,
    }
    output = artifact_root / "final_screening_report.json"
    if output.exists():
        raise FileExistsError(f"refusing to overwrite final screening report: {output}")
    with output.open("w", encoding="utf-8") as handle:
        json.dump(report, handle, ensure_ascii=False, indent=2, sort_keys=True)
        handle.write("\n")
    print(json.dumps(report, ensure_ascii=False, sort_keys=True))
    return 0 if survived else 2


if __name__ == "__main__":
    raise SystemExit(main())
